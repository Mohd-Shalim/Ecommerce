import java.util.TreeMap;

public class LinkedHashSetExample
{
	public static void main(String[] args)
	{
	TreeMap<String,String> linkedset = new TreeMap<String, String>();
		linkedset.put("A","jhbjhb");
		linkedset.put("B", "hjb");
		linkedset.put("C", "jgyug");
		linkedset.put("D", "jguyjgu");
		System.out.println(linkedset);
	}
}